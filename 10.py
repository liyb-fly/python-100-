"""
编写一个程序，它接受一系列以逗号分隔的4位二进制数作为输入，然后检查它们是否可被5整除。可被5整除的数字将按逗号分隔的顺序打印。

例子：

0100,0011,1010,1001

那么输出应该是：

1010

注意：假设数据是由控制台输入的。
"""
ls = input("please input").split(",")
ls2 = []
ls3 = []
for i in ls:
    j = int(i[0]) * (2 ** 3) + int(i[1]) * (2 ** 2) + int(i[2]) * (2 ** 1) + int(i[3]) * (2 ** 0)
    if (j % 5 == 0):
        ls2.append(j)
        ls3.append(i)

print(ls2)
print(ls3)
